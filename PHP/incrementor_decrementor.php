<?php
//Incrementor and Decrementor (Post, Pre)

//Post Incrementor ($X++)

$X = 5;
echo "Before Increment: " .$X . "\n";

$Y = $X++;
echo "value of Y: ".$Y . "\n";
echo "After Increment: " .$X;

echo "\n \n \n";


//Pre Incrementor
echo "Before Increment: " .$X . "\n";
$Y = ++$X;
echo "value of Y: ".$Y . "\n";
echo "After Increment: " .$X;

echo "\n \n \n";


//Pre Decrementor

